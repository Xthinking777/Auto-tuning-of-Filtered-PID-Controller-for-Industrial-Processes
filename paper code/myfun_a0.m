function y = myfun_a0(x,t1,t2)
% a1=1/109.4;
% a2=1/70.2;
% a3=1/66.5;
a1=-0.4;
a2=-0.35;
a3=-0.375;
K=1/(t2/pi);%K为衔接段函数的缩放比例,无需修改
cos_x1=((a1-a2)/2)*cos(K*(x-t1))+a1+(a2-a1)/2;%P1到P2的衔接部分
cos_x2=((a2-a3)/2)*cos(K*(x-2*t1-t2))+a2+(a3-a2)/2;%P2到P3的衔接部分
y=(a1).*(x>=0 & x<t1)+cos_x1.*(x>=t1 & x<t1+t2)+(a2).*(x>=t1+t2 & x<2*t1+t2)+cos_x2.*(x>=2*t1+t2 & x<2*t1+2*t2)+(a3).*(x>=2*t1+2*t2 & x<3*t1+2*t2);
end

