function y = myfun_b0(x,t1,t2)
% b1=1.02/109.4;
% b2=0.84/70.2;
% b3=0.91/66.5;
b1=0.91/66.5;
b2=0.84/70.2;
b3=1.02/109.4;
K=1/(t2/pi);
cos_x1=((b1-b2)/2)*cos(K*(x-t1))+b1+(b2-b1)/2;
cos_x2=((b2-b3)/2)*cos(K*(x-2*t1-t2))+b2+(b3-b2)/2;
y=(b1).*(x>=0 & x<t1)+cos_x1.*(x>=t1 & x<t1+t2)+(b2).*(x>=t1+t2 & x<2*t1+t2)+cos_x2.*(x>=2*t1+t2 & x<2*t1+2*t2)+(b3).*(x>=2*t1+2*t2 & x<3*t1+2*t2);
end


